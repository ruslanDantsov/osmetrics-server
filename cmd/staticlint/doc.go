/*
Package staticlint/main предоставляет multichecker для анализа Go-кода.

Как запустить:

	go run ./cmd/staticlint ./...

Вы можете указать путь к конкретному файлу или директории с пакетами.

Анализаторы, включённые в multichecker:

1. Стандартные анализаторы (golang.org/x/tools/go/analysis/passes):
  - asmdecl — проверяет правильность объявления ассемблерных функций
  - assign — проверяет корректность присваиваний
  - atomic — проверяет правильное использование sync/atomic
  - unusedresult — проверяет, используются ли возвращаемые значения

2. Staticcheck анализаторы (honnef.co/go/tools):
  - SAxxx — выявляют потенциальные ошибки и баги
  - ST1000 — проверяет корректность имени пакета

3. Публичные анализаторы:
  - github.com/kisielk/errcheck — проверяет, что ошибки от вызовов не игнорируются
  - github.com/ultraware/funlen — — проверяет, что длина функций не превышает заданный предел (100 символов)

4. Кастомный анализатор:
  - noosexit — запрещает прямой вызов os.Exit в функции main пакета main
*/
